 let animal = 'cat';
 console.log("Is the animal a cat? prediction True");
 console.log(animal === 'cat'); // True
 
 console.log("Is the animal a dog? Prediction: False");
 console.log(animal === 'dog'); // False
 
 console.log("Is the animal not a fish? Prediction: True");
 console.log(animal !== 'fish'); // True
 
 console.log("Is the animal not a cat? Prediction: False");
 console.log(animal !== 'cat'); // False
 
 let numCookies = 5;
 console.log("Do I have more than 3 cookies? Prediction: True");
 console.log(numCookies > 3); // True
 
 console.log("Do I have less than 2 cookies? Prediction: False");
 console.log(numCookies < 2); // False
 
 let isRaining = false;
 console.log("Is it raining? Prediction: False");
 console.log(isRaining); // False
 
 console.log("Is it not raining? Prediction: True");
 console.log(!isRaining); // True
 
